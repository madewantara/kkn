    <script>
        function doAfterSelectImage(input) {
            readURL(input);
            uploadUserProfileImage();
        }
        function readURL(input) {
            if (input.files && input.files[0]) {
                var reader = new FileReader();
                reader.onload = function (e) {
                    $('#image_user').css('background-image', 'url(' + e.target.result + ')');
                };
                reader.readAsDataURL(input.files[0]);
            }
        }
        function uploadUserProfileImage() {
            let myForm = document.getElementById('user_save_profile_form');
            let formData = new FormData(myForm);
            $.ajax({
                type: 'POST',
                data: formData,
                dataType: 'JSON',
                contentType: false,
                cache: false,
                processData: false,
                url: '{{route('save.profile.picture')}}',
            });
            location.reload();
            $('#image_user').css('background-image', 'url(' + '{{ asset("/storage/profile/".' + document.getElementById("profile_pic").files[0] + ') }}' + ')');
        }
    </script>
@endpush
